function buildSearchIndex(): Map<string, string[]> {
  const map = new Map<string, string[]>()
  for (const [emoji, keywords] of Object.entries(KEYWORD_TABLE)) {
    for (const kw of keywords) {
      const lower = kw.toLowerCase()
      const existing = map.get(lower)
      if (existing) {
        existing.push(emoji)
      } else {
        map.set(lower, [emoji])
      }
    }
  }
  return map
}

/** Maps lowercase keywords to arrays of emoji characters. Built once at import time. */
export const EMOJI_SEARCH: Map<string, string[]> = buildSearchIndex()
